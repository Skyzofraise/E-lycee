	<h4>Nos formations</h4>
	<p>
		Deux licences professionnelles construites en partenariat avec des universités constituent une autre voie complémentaire de réussite et de professionnalisation pour nos techniciens supérieurs.
	</p>
	<p>
		Cinq baccalauréats, un scientifique à deux spécialités (sciences de l'ingénieur et sciences et vie de la terre) et quatre STI2D (AC, EE, SIN, ITEC).
	</p>
	<p>
		Par ailleurs depuis la rentrée 2005, le lycée DIDEROT accueille des formations préparant au baccalauréat professionnel dans les domaines de l'étude et de la définition de produits industriels et des microtechniques. Depuis cette même date le lycée forme aux métiers de l'horlogerie et délivre le Brevet des Métiers d'Art ainsi que le CAP et depuis 2015 le Diplôme des Métiers d'Art. Nous proposons ainsi des perspectives de réussite pour les jeunes collégiens dans des secteurs d'activités porteurs.
		A la rentrée de 2012 le lycée a ouvert une ULIS PRO qui permet à des jeunes en situation de handicap de construire un parcours à la fois scolaire et professionnel.
	</p>
	<h4>Le GRETA</h4>
	<p>
		Le GRETA Industriel des Technologies Avancées (GPI2D) installé dans nos murs propose des formations en alternance et dans le cadre de la formation continue. Ces formations dans les domaines de la conception de produits, de l'électrotechnique de l'électronique de l'informatique et d'internet prennent appui sur les ressources matérielles et humaines du lycée DIDEROT.
	</p>
	<p>
		Les ressources tant humaines que matérielles dont dispose le lycée DIDEROT permettent d'assurer des opérations de conseil technologique en direction des entreprises.
	</p>
